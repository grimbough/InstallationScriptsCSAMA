## Installation of required Ubuntu libraries for installing CSAMA R packages
## tested on Ubuntu 16.04, you may need to change some library names for older versions

sudo apt-get install build-essential \
  libxml2-dev \
  libcurl4-openssl-dev \
  libcairo2-dev \
  libreadline-dev \
  libjpeg-dev \
  libtiff5-dev \
  libpng-dev \
  libssl-dev \
  libssh2-1-dev \
  libhunspell-dev \
  libnetcdf-dev \
  libpq-dev \
  libmariadb-client-lgpl-dev \
  libglu1-mesa-dev \
  libxt-dev \
  libudunits2-dev \
  libgdal-dev